fun main()
{
    val x="Hello World"
    println("$x is constant.")

    var a=10
    println("$a is Integer variable")

    var b="Hello"
    println("$b is String variable")

    var c=15.30f
    println("$c is Double variable")

    var d='P'
    println("$d is Character variable")

    var e=false
    println("$e is Boolian variable")

}